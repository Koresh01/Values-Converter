/// <summary>
/// Единицы измерения длины.
/// </summary>
public enum LengthUnit
{
    Meter,      // Метр (основная единица длины в СИ)
    Kilometer,  // Километр (1000 метров)
    Centimeter, // Сантиметр (0.01 метра)
    Millimeter, // Миллиметр (0.001 метра)
    Decimeter,  // Дециметр (0.1 метра) 
    Mile,       // Миля (1609.344 метров, английская миля)
    Yard,       // Ярд (0.9144 метра, английская единица)
    Foot        // Фут (0.3048 метра, английская единица)
}

/// <summary>
/// Единицы измерения Объёма.
/// </summary>
public enum VolumeUnit
{
    CubicMeter,     // Кубический метр (м³), основная единица объёма в СИ
    Liter,          // Литр (L), 1/1000 кубического метра
    Milliliter,     // Миллилитр (мл), 1/1000 литра
    CubicCentimeter,// Кубический сантиметр (см³), равен 1 миллилитру
    CubicMillimeter,// Кубический миллиметр (мм³), 1/1000 куб. см
    CubicFoot,      // Кубический фут (ft³), английская единица объёма
    CubicInch,      // Кубический дюйм (in³), английская единица объёма
    Gallon,         // Галлон (US gallon), 3.78541 литра
    Quart,          // Кварта (US quart), 1/4 галлона
    Pint            // Пинта (US pint), 1/8 галлона
}

/// <summary>
/// Единицы измерения температуры.
/// </summary>
public enum TemperatureUnit
{
    Celsius,        // Градусы Цельсия (°C) — базовая единица для повседневных измерений, используется в СИ
    Fahrenheit,     // Градусы Фаренгейта (°F) — англосаксонская система, вода замерзает при 32°F и кипит при 212°F
    Kelvin,         // Кельвины (K) — абсолютная шкала температуры, 0 K = -273.15°C
    Rankine,        // Градусы Ранкина (°R) — абсолютная шкала в англосаксонской системе, 0°R = 0 K
    Delisle,        // Градусы Делиля (°De) — историческая шкала, 0°De = 100°C, увеличение °De уменьшает температуру
    Newton,         // Градусы Ньютона (°N) — историческая шкала, 0°N = 0°C, 33°N = 100°C
    Réaumur,        // Градусы Реомюра (°Ré) — историческая шкала, 0°Ré = 0°C, 80°Ré = 100°C
    Rømer,          // Градусы Рёмера (°Rø) — историческая шкала, 0°Rø = -7.5°C, 60°Rø = 60°C
}

/// <summary>
/// Единицы измерения площади.
/// </summary>
public enum SquareUnit
{
    // --- СИ и метрическая система ---
    SquareMeter,        // Квадратный метр (м²) — базовая единица площади в СИ
    SquareKilometer,    // Квадратный километр (км²) = 1,000,000 м²
    SquareCentimeter,   // Квадратный сантиметр (см²) = 0.0001 м²
    SquareMillimeter,   // Квадратный миллиметр (мм²) = 1e-6 м²
    SquareMicrometer,   // Квадратный микрометр (мкм²) = 1e-12 м²
    Hectare,            // Гектар (га) = 10,000 м², используется в сельском хозяйстве
    Are,                // Ар (a) = 100 м², 1/100 гектара

    // --- Английская система ---
    SquareFoot,         // Квадратный фут (ft²) = 0.092903 м²
    SquareInch,         // Квадратный дюйм (in²) = 0.00064516 м²
    SquareYard,         // Квадратный ярд (yd²) = 0.836127 м²
    SquareMile,         // Квадратная миля (mi²) = 2.58999 × 10⁶ м²
    Acre,               // Акр (ac) = 4,046.8564224 м²

    // --- Морские и географические единицы ---
    SquareNauticalMile, // Квадратная морская миля (nmi²) = 3.429904 × 10⁶ м²
    Barn,               // Барн (b) = 1e-28 м², используется в ядерной физике для измерения сечения рассеяния

    // --- Азиатские традиционные единицы ---
    Tsubo,              // Цубо (坪, Япония) = 3.305785 м²
    Pyeong,             // Пён (평, Корея) = 3.305785 м² (тот же размер, что цубо)
    Mu,                 // Му (亩, Китай) = 666.6667 м²
    Rai,                // Рай (ไร่, Таиланд) = 1600 м²
    Dunam,              // Дунам (متر مربع, Ближний Восток) = 1000 м²
    Ping,               // Пин (坪, Тайвань) = 3.305785 м²
}

/// <summary>
/// Единицы измерения частоты.
/// </summary>
public enum FrequencyUnit
{
    // --- СИ и метрическая система ---
    Hertz,              // Герц (Гц, Hz) — базовая единица частоты в СИ = 1 цикл/секунда
    Kilohertz,          // Килогерц (кГц, kHz) = 1,000 Гц
    Megahertz,          // Мегагерц (МГц, MHz) = 1,000,000 Гц
    Gigahertz,          // Гигагерц (ГГц, GHz) = 1,000,000,000 Гц
    Terahertz,          // Терагерц (ТГц, THz) = 1,000,000,000,000 Гц
    Millihertz,         // Миллигерц (мГц, mHz) = 0.001 Гц
    Microhertz,         // Микрогерц (мкГц, μHz) = 1e-6 Гц
    Nanohertz,          // Наногерц (нГц, nHz) = 1e-9 Гц

    // --- Угловая частота ---
    RadianPerSecond,    // Радиан в секунду (рад/с) — используется для угловой частоты ω = 2πf

    // --- Музыкальные и звуковые ---
    BeatsPerMinute,     // Удары в минуту (BPM) — используется в музыке и метрономах (1 BPM = 1/60 Гц)
    CyclesPerMinute,    // Циклы в минуту (CPM) = 1/60 Гц

    // --- Астрономические и геофизические ---
    RevolutionsPerSecond,   // Оборотов в секунду (об/с, rps) = 1 Гц
    RevolutionsPerMinute,   // Оборотов в минуту (об/мин, rpm) = 1/60 Гц
    PerDay, // В день (1/день) = 1/86400 Гц, используется в геофизике и астрономии
}



/// <summary>
/// Единицы измерения мощности.
/// </summary>
public enum PowerUnit
{
    // --- СИ и метрическая система ---
    Watt,               // Ватт (Вт, W) — базовая единица мощности в СИ = 1 Дж/с
    Kilowatt,           // Киловатт (кВт, kW) = 1,000 Вт
    Megawatt,           // Мегаватт (МВт, MW) = 1,000,000 Вт
    Gigawatt,           // Гигава́тт (ГВт, GW) = 1,000,000,000 Вт
    Milliwatt,          // Милливатт (мВт, mW) = 0.001 Вт
    Microwatt,          // Микроватт (мкВт, μW) = 1e-6 Вт
    Nanowatt,           // Нановатт (нВт, nW) = 1e-9 Вт

    // --- Электрические и тепловые ---
    Horsepower,         // Лошадиная сила (hp, механическая) = 745.7 Вт
    MetricHorsepower,   // Метрическая лошадиная сила (ps) = 735.49875 Вт
    ElectricalHorsepower, // Электрическая л.с. = 746 Вт
    BoilerHorsepower,   // Котельная л.с. (boiler hp) = 9,809.5 Вт
    CaloriePerSecond,   // Калория в секунду (кал/с) = 4.184 Вт
    CaloriePerMinute,   // Калория в минуту (кал/мин) = 4.184 / 60 ≈ 0.069733 Вт
    BTUPerHour,         // Британская тепловая единица в час (BTU/h) = 0.29307107 Вт

    // --- Астрономические ---
    SolarLuminosity,    // Светимость Солнца (L☉) = 3.828 × 10²⁶ Вт

    // --- Другие ---
    ErgPerSecond,       // Эрг в секунду (эрг/с) = 1e-7 Вт
    FootPoundPerSecond, // Фут-фунт в секунду (ft·lb/s) = 1.3558179483 Вт
}


/// <summary>
/// Единицы измерения давления.
/// </summary>
public enum PressureUnit
{
    // --- СИ и метрическая система ---
    Pascal,                 // Паскаль (Па, Pa) — базовая единица давления в СИ = 1 Н/м²
    Kilopascal,             // Килопаскаль (кПа, kPa) = 1,000 Па
    Megapascal,             // Мегапаскаль (МПа, MPa) = 1,000,000 Па
    Gigapascal,             // Гигапаскаль (ГПа, GPa) = 1,000,000,000 Па
    Hectopascal,            // Гектопаскаль (гПа, hPa) = 100 Па (часто используется в метеорологии)
    Bar,                    // Бар (bar) = 100,000 Па
    Millibar,               // Миллибар (mbar) = 100 Па (то же, что гектопаскаль)
    TechnicalAtmosphere,    // Техническая атмосфера (ат, at) = 98,066.5 Па
    StandardAtmosphere,     // Физическая атмосфера (атм, atm) = 101,325 Па

    // --- Английская и американская системы ---
    Psi,                    // Фунт силы на квадратный дюйм (psi) = 6,894.757 Па
    Torr,                   // Торр (мм рт. ст.) = 133.322 Па
    InchOfMercury,          // Дюйм ртутного столба (inHg) = 3,386.389 Па
    InchOfWater,            // Дюйм водяного столба (inH₂O) = 249.0889 Па
    PoundPerSquareFoot,     // Фунт силы на квадратный фут (psf) = 47.88026 Па

    // --- Другие и технические ---
    DynePerSquareCentimeter,// Дина на квадратный сантиметр (dyn/cm²) = 0.1 Па
    KilogramForcePerSquareCentimeter, // кгс/см² = 98,066.5 Па
    MillimeterOfMercury,    // Миллиметр ртутного столба (мм рт. ст., mmHg) = 133.322 Па
    Barye,                  // Барий (Ba) = 0.1 Па (единица CGS)
}


